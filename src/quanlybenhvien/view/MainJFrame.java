    public static Date stringToDate(String Date) throws ParseException{
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        java.util.Date parsed = format.parse(Date);
        Date sql = new Date(parsed.getTime());
        return sql;
    }
